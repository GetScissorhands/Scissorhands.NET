namespace Scissorhands.ViewModels.Post
{
    /// <summary>
    /// This specifies the page type.
    /// </summary>
    public enum PageType
    {
        /// <summary>
        /// Indicates that no page type is identified.
        /// </summary>
        Undefined = 0,

        /// <summary>
        /// Indicates that post is the page type.
        /// </summary>
        Post = 1,

        /// <summary>
        /// Indicates that page is the page type.
        /// </summary>
        Page = 2
    }
}